public class SearchItemAdapter extends BaseQuickAdapter<MeijuBean,BaseViewHolder> {
    private Context context;
    public SearchItemAdapter(Context context) {
        super(R.layout.item_search_result);
        this.context=context;
    }
    @Override
    protected void convert(BaseViewHolder helper, MeijuBean item) {
        helper.setText(R.id.tv_name,item.getMeiju_name())
                .setText(R.id.tv_time,item.getMeijiu_time());
        x.image().bind((ImageView) helper.getView(R.id.iv_icon), item.getMeiju_imgurl());
    }

}
